def get_translation(lang_code, key, default=None):
    """
    Get a translated string for a specific language.

    Args:
        lang_code: Language code (e.g., 'en', 'zh')
        key: Translation key (e.g., 'dashboard')
        default: Default value if translation not found

    Returns:
        Translated string or default value
    """
    if lang_code not in MENU_TRANSLATIONS:
        lang_code = 'en'

    translations = MENU_TRANSLATIONS.get(lang_code, MENU_TRANSLATIONS['en'])
    return translations.get(key, default or key)
